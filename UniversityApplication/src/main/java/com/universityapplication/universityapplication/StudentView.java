    private void friendButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_friendButtonActionPerformed

        PreparedStatement pst; 
        try {
            if (!friend) {
                pst = conn.prepareStatement("INSERT INTO `friends` (`userId`, `friendId`) VALUES (?, ?);");
                pst.setInt(1, current.getId());
                pst.setInt(2, user.getId());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Added succesfully");
                friendButton.setText("Remove Friend");
            } else {
                pst = conn.prepareStatement("DELETE FROM `friends` WHERE userId=? and friendId=?;");
                pst.setInt(1, current.getId());
                pst.setInt(2, user.getId());
                pst.execute();
                JOptionPane.showMessageDialog(null, "Removed  succesfully");
                friendButton.setText("Add Friend");

            }
            friend = !friend;
            checkFriend();
        } catch (SQLException ex) {
        }
    }//GEN-LAST:event_friendButtonActionPerformed

