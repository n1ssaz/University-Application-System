    void showSubjects() {
        DefaultListModel<String> model = new DefaultListModel<>();
        for (Subject sub : subjectList) {
            String common = "";
            for (Subject el : userSubjectList) {
                if (el.getId() == sub.getId()) {
                    common = "(common)";
                    break;
                }
            }
            model.addElement(sub.getName() + " " + common);
        }
        jList1.setModel(model);
    }

